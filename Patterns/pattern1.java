public class pattern1 {
    public static void main(String[] args) {
        int n=4;
        int m=5;
        for(int i = 1; i <= n; i++) {  // here we created 2 loops called nested loops i for vertical 
            for (int j = 1;  j<= m; j++) {//and j for horizantal
                System.out.print("*");//we write print instead of println because we want horizantal * in one line
                
            }
            System.out.println();//this is written to go to next 
            
        }}
        
    }


